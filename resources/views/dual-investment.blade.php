        <div class="faq-item" style="padding: 16px 0;">
            <div onclick="toggleFaq(this)" style="cursor: pointer; display: flex; justify-content: space-between; align-items: center;">
                <span style="font-size: 20px; font-weight: 600; color: #1e2329;">What are “Subscription Amount”, “Target Price”, “Settlement Date”, “Deposit Currency”, “Target Currency”, “Subscription Period”, and “Fixing Price”?</span>
                <span style="font-size: 26px;">+</span>
            </div>
            <div class="faq-answer" style="display: none; padding-top: 8px; font-size: 16px; color: #5e6673;">
                • Subscription Amount: Amount which you have deposited when subscribing to Dual Investment. This amount will be used to buy or sell crypto.
                <br><br>
                • Target Price: A set price at which you want to buy or sell cryptocurrency.
                <br><br>
                • Settlement Date: A set date at which you want to buy or sell cryptocurrency. You will also receive your rewards on this date. The Settlement for BTC, ETH, and BNB occurs every Tuesday and Friday. Other tokens are on every Friday.
                <br><br>
                • Deposit Currency: The currency you used to subscribe to a Dual Investment product.
                <br><br>
                • Target Currency: The asset you will be receiving if the Target Price is reached.
                <br><br>
                • Subscription Period/Duration: The number of days from the day of the subscription until the Settlement Date.
                <br><br>
                • Fixing Price: An average of the Market Price in the last 30 minutes before 08:00 (UTC) on the Settlement Date. Fixing Price and Target Price determine whether your investment will be converted into the Target Currency.
            </div>
        </div>

        <div class="faq-item" style="padding: 16px 0;">
            <div onclick="toggleFaq(this)" style="cursor: pointer; display: flex; justify-content: space-between; align-items: center;">
