                Sell High: <br>
                1.  If the Target Price is reached on the settlement date: <br>
                •
                (Subscription Amount + Rewards) * Target Price <br>
                2.  If the Target Price is not reached on the settlement date: <br>
                •
                Subscription Amount + Rewards
